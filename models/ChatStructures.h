#ifndef CHATSTRUCTURES_H
#define CHATSTRUCTURES_H

#include <QString>
#include <QDateTime>

/**
 * @brief Структура для представлення одного повідомлення в чаті.
 *
 * Ця структура використовується як DTO (Data Transfer Object) для передачі
 * даних повідомлення між базою даних, логікою (ChatManager) та інтерфейсом (ChatPageWidget).
 * Вона містить всю необхідну інформацію для відображення одного "бабла" в історії листування.
 */
struct ChatMessage {
    /**
     * @brief Унікальний ідентифікатор користувача-відправника.
     * Якщо це повідомлення від "Я", то цей ID співпадає з поточним користувачем.
     */
    int fromUserId;

    /**
     * @brief Унікальний ідентифікатор користувача-отримувача.
     */
    int toUserId;

    /**
     * @brief Текстовий вміст повідомлення.
     * Підтримує Unicode (включно з емодзі, кирилицею тощо).
     */
    QString message;

    /**
     * @brief Точна дата та час відправлення повідомлення.
     * Використовується для сортування історії та відображення часу в інтерфейсі.
     */
    QDateTime timestamp;
};

#endif // CHATSTRUCTURES_H