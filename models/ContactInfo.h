#ifndef CONTACTINFO_H
#define CONTACTINFO_H

#include <QString>

/**
 * @brief Клас для зберігання контактної інформації користувача.
 *
 * Цей клас слугує контейнером для особистих контактних даних, таких як
 * номер телефону та адреса електронної пошти. Він розроблений для використання
 * як складова частина (композиція) більш складного об'єкта UserProfile.
 *
 * @see UserProfile
 */
class ContactInfo {
public:
    /**
     * @brief Конструктор за замовчуванням та з параметрами.
     *
     * Дозволяє створити об'єкт ContactInfo як порожнім, так і з попередньо
     * заповненими даними.
     *
     * @param phone Номер телефону користувача (за замовчуванням порожній рядок).
     * @param email Адреса електронної пошти користувача (за замовчуванням порожній рядок).
     */
    ContactInfo(const QString &phone = "", const QString &email = "");

    // --- Геттери (Методи доступу) ---

    /**
     * @brief Отримує збережений номер телефону.
     * @return QString Рядок, що містить номер телефону.
     */
    QString getPhone() const;

    /**
     * @brief Отримує збережену адресу електронної пошти.
     * @return QString Рядок, що містить email.
     */
    QString getEmail() const;

    // --- Сеттери (Методи модифікації) ---

    /**
     * @brief Встановлює або оновлює номер телефону.
     *
     * @note Метод просто зберігає рядок і не виконує перевірку формату (валідацію) телефону.
     * Валідація повинна виконуватись на рівні UI або валідатора форми.
     *
     * @param newPhone Новий номер телефону.
     */
    void setPhone(const QString &newPhone);

    /**
     * @brief Встановлює або оновлює адресу електронної пошти.
     *
     * @note Метод не перевіряє валідність email (наявність @ тощо).
     *
     * @param newEmail Нова адреса email.
     */
    void setEmail(const QString &newEmail);

private:
    QString m_phone;    ///< Приватне поле для зберігання номера телефону.
    QString m_email;    ///< Приватне поле для зберігання адреси email.
};

#endif //CONTACTINFO_H